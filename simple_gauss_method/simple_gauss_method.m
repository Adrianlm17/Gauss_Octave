clc;



% Matrix and results given
A = [1,3,0;0,1,4;2,3,-2;];
B = [1;1;1];

function results = simple_gauss_method(A, B)
  results = "";

  % Upper Triangular
  for i = 2:size(A, 1)
    for j = 1:i-1
      if A(i,j) != 0
        x = A(i,j);
        y = A(j,j);

        multiplier = x / y;

        A(i, :) = A(i, :) - multiplier * A(j, :);
        B(i) = B(i) - multiplier * B(j);
      endif
    endfor
  endfor


  % Find values ​​of the unknowns
  for i = size(A,1):-1:1
    if i == size(A,1)
      result = B(i)/A(i,i);
      for j = i:-1:1
        A(j,i);
        A(j,i) = A(j,i)*result;
      endfor

    else
      operation = 0;
      for j = i+1:size(A,1)
        operation = operation + (-1*A(i,j));
      endfor

      result = ((B(i)+operation)/A(i,i));
      for j = i:-1:1
        A(j,i);
        A(j,i) = A(j,i)*result;
      endfor
    endif

    results = [results, sprintf("X%d = %f\n", i, result)];
  endfor
endfunction



results = simple_gauss_method(A, B);

disp(results);
